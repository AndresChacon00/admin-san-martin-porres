      'Contribución por discapacidad de hijos',
      'Seguro Social-Regimen prestacional de empleo',
      'Porcentaje SSO',
      'Porcentaje RPE',
      'FAOV',
      'Porcentaje FAOV',
      'Pago directo',
      'Jubilado',
      'Cuenta Bancaria',
      'Observaciones',
      'fecha de registro',
      'fecha de actualización',
