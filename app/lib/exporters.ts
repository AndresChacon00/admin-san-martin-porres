      'Prima Antigüedad',
      'Prima Antigüedad según SISTEMA',
      'Beneficio Geográfica',
      'Prima Geográfica',
      'Prima Geográfica según SISTEMA',
      'Prima Compensación Académica',
      'Prima Compensación Académica según SISTEMA',
      'Beneficio de prima por hijo',
